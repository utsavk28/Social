import mongooose from 'mongoose';

const UserSchema = new mongooose.Schema({
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    date: {
        type: Date,
        default: Date.now,
    },
});

var User = mongooose.model('user', UserSchema);

export default User;
